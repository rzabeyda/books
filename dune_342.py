count = 0
for book in books:
    bid = book.get("id")
    if bid in additions:
        life_list = additions[bid]
        thoughts = book.get("thoughts", [])
        for i, t in enumerate(thoughts):
            if i < len(life_list) and "**В жизни:**" not in t.get("example", ""):
                t["example"] = t["example"].rstrip() + "\n\n**В жизни:** " + life_list[i]
                count += 1

with open("/root/books/webapp/books.json", "w", encoding="utf-8") as f:
    json.dump(books, f, ensure_ascii=False, separators=(",", ":"))
print(f"Updated {count} thoughts")
