import json

with open('/root/books/webapp/books.json', encoding='utf-8') as f:
    books = json.load(f)

# Маппинг жанров по ID
genre_changes = {
    # politics → политика
    39: 'политика', 44: 'политика', 54: 'политика', 55: 'политика',
    57: 'политика', 70: 'политика', 100: 'политика',

    # religion → священные писания
    40: 'священные писания', 41: 'священные писания', 42: 'священные писания',
    52: 'священные писания', 53: 'священные писания', 87: 'священные писания',

    # → философия
    59: 'философия',   # Искусство войны
    7:  'философия',   # Чёрный лебедь
    10: 'философия',   # Антихрупкость
    11: 'философия',   # Одураченные случайностью
    12: 'философия',   # Рискуя собственной шкурой
    3:  'философия',   # Голая обезьяна
    91: 'философия',   # Суждения и беседы (Конфуций)
    81: 'философия',   # Атлант расправил плечи
    9:  'философия',   # Homo Deus (was id 104)

    # → наука
    56: 'наука',       # Происхождение видов
    102: 'наука',      # Краткая история времени
    104: 'наука',      # Homo Deus

    # → фэнтези
    77: 'фэнтези',     # Гарри Поттер
    78: 'фэнтези',     # Властелин колец
    140: 'фэнтези',    # Хоббит
}

changed = 0
for b in books:
    if b['id'] in genre_changes:
        old = b['genre']
        new = genre_changes[b['id']]
        if old != new:
            print(f"  {b['id']:3} | {old} → {new} | {b['title']}")
            b['genre'] = new
            changed += 1

with open('/root/books/webapp/books.json', 'w', encoding='utf-8') as f:
    json.dump(books, f, ensure_ascii=False, indent=2)

print(f'\nИзменено: {changed}')
