import json
with open("/root/books/webapp/books.json") as f:
    books = json.load(f)

appends = {
    40: {4: " Она работала тогда — и работает сейчас."},
    42: {
        0: " Это не опция — это требование.",
        2: " Приоритет жизни меняет всё.",
        8: " Слова убивают быстро."
    },
    52: {
        0: " Принцип работает в любом деле.",
        1: " Измена своему пути не спасает от тревоги — она её усиливает. Жить чужой жизнью — дорогостоящая ошибка.",
        2: " Джнана, карма, бхакти — три разных входа в одну дверь. Твоя природа — твой ключ.",
        3: " Атман не умирает с телом — это не вера, а прямое переживание медитирующего.",
        4: " Движение от тамаса к саттве — практическая программа любой трансформации.",
        5: " Равновесие — тренируемый навык.",
        6: " Йога — не поза, а состояние слияния с тем, что делаешь."
    },
    53: {2: " Непостоянство — закон, а не исключение."}
}

count = 0
for b in books:
    bid = b["id"]
    if bid not in appends:
        continue
    for i, t in enumerate(b.get("thoughts", [])):
        if i in appends[bid]:
            t["example"] += appends[bid][i]
            count += 1

with open("/root/books/webapp/books.json", "w", encoding="utf-8") as f:
    json.dump(books, f, ensure_ascii=False, separators=(",",":"))
print(f"Extended {count} thoughts")
