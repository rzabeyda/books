#!/usr/bin/env python3
import json

PATH = "/root/books/webapp/books.json"

descriptions = {
    "Размышления": "Личный дневник императора Рима — стоическая философия как ежедневная практика: как сохранять достоинство, терпение и ясность ума в любых обстоятельствах.",
    "Государство": "Платон строит идеальное общество и задаёт вопросы о справедливости, власти и природе человека — на которые мы до сих пор не нашли ответов.",
    "Миф о Сизифе": "Камю исследует главный философский вопрос — зачем жить в бессмысленном мире — и приходит к неожиданному ответу: надо представить Сизифа счастливым.",
    "Рассуждение о методе": "Декарт сомневается во всём что можно — и находит единственную несомненную точку: «Я мыслю, следовательно, существую», заложив фундамент современной философии.",
    "Этика": "Спиноза доказывает геометрическим методом: Бог и природа — одно, свобода воли иллюзорна, а путь к счастью — понимание своих эмоций и законов мироздания.",
}

data = json.load(open(PATH, encoding="utf-8"))
updated = 0
for book in data:
    if book["title"] in descriptions and "description" not in book:
        book["description"] = descriptions[book["title"]]
        updated += 1
        print(f"Updated: {book['title']}")

json.dump(data, open(PATH, "w", encoding="utf-8"), ensure_ascii=False, indent=2)
print(f"Done. Updated {updated} books.")
