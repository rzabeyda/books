# Trim > 820 at sentence boundary
ids = [313,314,315,316,317,318,319,320]
changed = False
for book in data:
    if book['id'] not in ids:
        continue
    for t in book['thoughts']:
        ex = t['example']
        if len(ex) > 820:
            pos = ex[:815].rfind('.')
            if pos > 700:
                t['example'] = ex[:pos+1]
                changed = True

if changed:
    with open('/root/books/webapp/books.json', 'w') as f:
        json.dump(data, f, ensure_ascii=False)

for book in data:
    if book['id'] in ids:
        lens = [len(t['example']) for t in book['thoughts']]
        bad = [(i,l) for i,l in enumerate(lens) if l < 700 or l > 820]
        status = 'OK' if not bad else f'BAD={bad}'
        print(f'ID={book["id"]:3d} {book["title"][:20]:20s}: min={min(lens)} max={max(lens)} {status}')
