with open('/root/books/webapp/books.json', 'r', encoding='utf-8') as f:
    books = json.load(f)

for b in books:
    if b.get('genre') != 'политика':
        continue
    bid = b['id']
    if bid in desc_fixes:
        b['description'] = desc_fixes[bid]
    for t in b['thoughts']:
        t['example'] = trim_to_fit(t['title'], t['example'])

