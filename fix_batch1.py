import json, re

def trim(text, max_len):
    if len(text) <= max_len:
        return text
    cut = text[:max_len]
    for sep in ['. ', '! ', '? ']:
        idx = cut.rfind(sep)
        if idx > max_len - 80:
            return cut[:idx+1]
    return cut.rstrip()

