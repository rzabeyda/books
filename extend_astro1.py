def trim(title, example, mx=800):
    if len(title) + len(example) <= mx:
        return example
    budget = mx - len(title)
    chunk = example[:budget]
    last = max(chunk.rfind("."), chunk.rfind("!"), chunk.rfind("?"))
    return example[:last+1] if last > 0 else example[:budget]

