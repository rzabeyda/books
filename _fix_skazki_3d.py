def trim_to_fit(title, example, max_total=800):
    total = len(title) + len(example)
    if total <= max_total:
        return example
    max_ex = max_total - len(title)
    chunk = example[:max_ex]
    for sep in ['. ', '! ', '? ']:
        pos = chunk.rfind(sep)
        if pos > max_ex - 200:
            return chunk[:pos+1]
    pos = chunk.rfind(' ')
    return chunk[:pos] + '.'

