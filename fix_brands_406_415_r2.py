ids = list(range(406, 416))
changed = False
for book in data:
    if book['id'] not in ids:
        continue
    for t in book['thoughts']:
        ex = t['example']
        if len(ex) > 820:
            pos = ex[:815].rfind('.')
            if pos > 700:
                t['example'] = ex[:pos+1]
                changed = True

if changed:
    with open('/root/books/webapp/books.json', 'w') as f:
        json.dump(data, f, ensure_ascii=False)

for book in data:
    if book['id'] in ids:
        dlens = len(book['description'])
