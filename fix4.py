with open("/root/books/webapp/books.json") as f:
    books = json.load(f)

target_ids = set(appends.keys())
issues = []

for b in books:
    bid = b["id"]
    if bid not in target_ids:
        continue
    for i, t in enumerate(b.get("thoughts", [])):
        if i in appends[bid]:
            t["example"] += appends[bid][i]
        if bid in target_ids:
            total = len(t["title"]) + len(t["example"])
            if total < 600 or total > 800:
                issues.append(f"Book {bid} t{i}: {total} chars")

if issues:
    print("ISSUES:")
    for iss in issues:
        print(" ", iss)
    print("NOT SAVED")
else:
    with open("/root/books/webapp/books.json", "w", encoding="utf-8") as f:
        json.dump(books, f, ensure_ascii=False, separators=(",", ":"))
    print("OK - patched and saved")
